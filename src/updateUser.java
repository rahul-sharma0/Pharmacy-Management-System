    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      
        
        int check_user_exist = 0;
        String username = txtusername.getText();
        
        if(username.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Email-id is required");
        }
        else
        {
            SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
            try
            {
                Connection con = ConnectionProvider.getCon();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from appuser where USERNAME='"+username+"'");
                while(rs.next())
                {
                    check_user_exist = 1;
                    txtusername.setEditable(false);
                    
                    
                        txtname.setEnabled(true);
                        txtaddress.setEnabled(true);
                        txtcontact.setEnabled(true);
                        txtdate.setEnabled(true);
                        txtrole.setEnabled(true);
                        txtemail.setEnabled(true);
                        jButton2.setEnabled(true);
                    
                    
                    txtname.setText(rs.getString("NAME"));
                    txtemail.setText(rs.getString("EMAIL"));
                    txtcontact.setText(rs.getString("CONTACTNO"));
                    txtaddress.setText(rs.getString("ADDRESS"));
                    String dob = rs.getString("DOB");
                    
                    if(dob != "")
                    {
                        try {
                            txtdate.setDate(dFormat.parse(dob));
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, e);
                        }
                    }
                    
                    if(rs.getString("ROLE").equals("ADMIN"))
                    {
                        txtrole.removeAllItems();
                        txtrole.addItem("ADMIN");
                        txtrole.addItem("PHARMACIST");
                    }
                    else{
                        txtrole.removeAllItems();
                        txtrole.addItem("PHARMACIST");
                        txtrole.addItem("ADMIN");
                    }
                }
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
            
            if(check_user_exist ==0)
            {
                JOptionPane.showMessageDialog(null, "Username not found!",null,JOptionPane.WARNING_MESSAGE);
            }
        }
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

