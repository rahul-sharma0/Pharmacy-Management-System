    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String userRole =(String) txtrole.getSelectedItem();
        String name = txtname.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date date = txtdate.getDate();
        String DOB = "";
        
        if(date != null)
        {
            DOB = dFormat.format(txtdate.getDate());
        }
        
        String Contact = txtcontact.getText();
        String email = txtemail.getText();
        String username = txtusername.getText();
        String address = txtaddress.getText();
        
        if(name.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Name is required");
        }
        else if(DOB.equals(""))
        {
             JOptionPane.showMessageDialog(null, "Date of Birth is required");
        }
        else if (Contact.equals(""))
        {
             JOptionPane.showMessageDialog(null, "Contact number is required");
        }
        else if(!Contact.matches(contactPattern) || Contact.length()!=10)
        {
             JOptionPane.showMessageDialog(null, "Contact number is invalid");
        }
        else if(email.equals(""))
        {
             JOptionPane.showMessageDialog(null, "Email-id is required");
        }
        else if (!email.matches(emailPattern))
        {
             JOptionPane.showMessageDialog(null, "Email-id is invalid");
        }
        else if(username.equals(""))
        {
             JOptionPane.showMessageDialog(null, "Username is required");
        }
        else if(address.equals(""))
        {
             JOptionPane.showMessageDialog(null, "Address is required");
        }
        else
        {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update appuser set ROLE=?, NAME=?, CONTACTNO=?,ADDRESS=?, EMAIL=?, DOB=? where USERNAME=?");
                
                ps.setString(1, userRole);
                ps.setString(2, name);
                ps.setString(3, Contact);
                ps.setString(4, address);
                ps.setString(5, email);
                ps.setString(6, DOB);
                ps.setString(7, username);
                
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "User updated Successfully","Success",JOptionPane.INFORMATION_MESSAGE);
                setVisible(false);
                  new updateUser().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

